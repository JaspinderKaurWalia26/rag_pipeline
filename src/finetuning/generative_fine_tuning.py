import os
os.makedirs("dataset", exist_ok=True)

from transformers import AutoTokenizer, AutoModelForCausalLM
from datasets import load_dataset
from transformers import TrainingArguments, Trainer
import torch

# Model 
model_name = "TinyLlama/TinyLlama-1.1B-Chat-v1.0"

# Load tokenizer
tokenizer = AutoTokenizer.from_pretrained(model_name)
tokenizer.pad_token = tokenizer.eos_token

# Load model
model = AutoModelForCausalLM.from_pretrained(
    model_name,
    device_map="auto"
)

# Enable gradient checkpointing (reduces memory)
model.gradient_checkpointing_enable()

# Load dataset
dataset = load_dataset(
    "json",
    data_files={
        "train": "/content/dataset/train.jsonl",
        "test": "/content/dataset/test.jsonl"
    }
)

# Tokenization
def tokenize(example):

    tokenized = tokenizer(
        example["text"],
        truncation=True,
        padding="max_length",
        max_length=256   
    )

    tokenized["labels"] = tokenized["input_ids"].copy()

    return tokenized


tokenized_dataset = dataset.map(tokenize, remove_columns=["text"])


# Training parameters
training_args = TrainingArguments(
    output_dir="./techcorp-model",
    num_train_epochs=3,
    per_device_train_batch_size=1,   
    gradient_accumulation_steps=4,   
    logging_steps=5,
    save_steps=50,
    learning_rate=2e-5,
    save_total_limit=2
)

# Trainer
trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=tokenized_dataset["train"]
)

# Clear GPU memory
torch.cuda.empty_cache()

# Train
trainer.train()

# Save model
trainer.save_model("techcorp-finetuned-model")
tokenizer.save_pretrained("techcorp-finetuned-model")



