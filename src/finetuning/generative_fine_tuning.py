def save_jsonl(df, filename):

    with open(filename, "w") as f:

        for _, row in df.iterrows():

            entry = {

                "text": f"""
### Instruction:
{row['prompt']}

### Response:
{row['completion']}
"""
            }

            f.write(json.dumps(entry) + "\n")


